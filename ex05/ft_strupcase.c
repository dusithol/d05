char *ft_strupcase(char *str)
{
    int d;
    //char *answer = str;
    d = 0;

    if(str[d] >= 'a' && str[d] <= 'z')
    {
        str[d] -= 32;
        d++;
    }

    while(str[d])
    {
        if (str[d] == ' ')
        {
            d++;
            str[d] -= 32;
        }

        d++;
    }

    return (str);

}

