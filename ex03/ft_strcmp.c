int		ft_strcmp(char *s1, char *s2)
{
	int d = 0;
	int sum_one = 0;
	int sum_two = 0;
	while(s1[d] != '\0')
	{
		sum_one += s1[d];
		d++;
	}
	d = 0;
	while(s2[d] != '\0')
	{
		sum_two += s2[d];
		d++;
	}
	return sum_one - sum_two;
}
