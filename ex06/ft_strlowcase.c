char *ft_strlowcase(char *str)
{
    int d;
       d = 0;
    
    
    
    while(str[d])
    {
        if(str[d] >= 'A' && str[d] <= 'Z')
        {
                str[d] += 32;
        }
        
        d++;
    }
    
    return (str);
      
}

